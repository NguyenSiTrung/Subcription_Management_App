// Helper functions for creating gradients
fun createHorizontalGradient(colors: List<Color>): Brush {
    return Brush.horizontalGradient(colors = colors)
}

fun createVerticalGradient(colors: List<Color>): Brush {
    return Brush.verticalGradient(colors = colors)
}

fun createRadialGradient(colors: List<Color>): Brush {
    return Brush.radialGradient(colors = colors)
}

fun createLinearGradient(
    start: Offset = Offset.Zero,
    end: Offset = Offset.Infinite,
    colors: List<Color>
): Brush {
    return Brush.linearGradient(
        colors = colors,
        start = start,
        end = end
    )
}
